namespace app\utils\widgets;


use app\models\Canvasser;
use Yii;
use yii\base\Widget;

class CanvasserView extends Widget
{
    /* @var \app\models\Canvasser */
    public $canvasser;

    public function init()
    {
        parent::init(); // TODO: Change the autogenerated stub
    }

    public function run()
    {
        if ($this->canvasser == null) {
            return '<span class="not-set">(not set)</span>';
        }

        $supp = Canvasser::findOne($this->canvasser);
        if ($supp == null) {
            return '<span class="not-set">(not set)</span>';
        }
        $suppUrl = Yii::$app->getUrlManager()->createUrl(['canvasser/view', 'id' => $this->canvasser]);
        $out = '<a href="'.$suppUrl.'" style="color: #1e70bf; margin-right: 20px;"><b>'.$supp->name .'</b></a>';
        return $out;
    }


}